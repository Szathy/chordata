def find_root_bone(bones):
	root = None
	for b in bones:
		if not b.parent:
			if root:
				raise Error("more than one Root bone found.")

			root = b

	if root:
		return root

	raise Error("No Root bone found.")

def find_lowest_bone(bones):
	lowest = 0
	for b in bones:
		lowest = min(lowest, b.matrix_local.to_translation().z)
	return lowest

